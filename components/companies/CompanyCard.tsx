"use client";

import { CompanyWithRelations } from "@/lib/validators/companies";
import { cn } from "@/lib/utils";
import { Globe, Mail, Phone } from "lucide-react";

export function CompanyCard({ company }: { company: CompanyWithRelations }) {
  const isClient = company.projects.length > 0;

  return (
    <div className="bg-white border border-slate-200/75 rounded-3xl p-6 flex flex-col h-full transition-all duration-300 hover:border-indigo-300 hover:shadow-xl hover:shadow-indigo-500/5 hover:-translate-y-1">

      {/* Top row: avatar + name + badge */}
      <div className="flex items-start justify-between gap-3 mb-5">
        <div className="flex items-center gap-3 min-w-0">
          <div className="h-11 w-11 rounded-2xl bg-gradient-to-br from-indigo-50 to-blue-50 border border-indigo-100/50 flex items-center justify-center text-indigo-600 font-extrabold text-lg shadow-sm shrink-0">
            {company.name.charAt(0).toUpperCase()}
          </div>
          <div className="min-w-0">
            <p className="font-bold text-slate-900 truncate leading-tight">{company.name}</p>
            {company.business_sector ? (
              <p className="text-xs text-slate-400 font-medium mt-0.5 truncate">{company.business_sector}</p>
            ) : (
              <p className="text-xs text-slate-300 italic mt-0.5">Secteur non défini</p>
            )}
          </div>
        </div>
        <span
          className={cn(
            "shrink-0 text-[10px] font-bold uppercase tracking-widest px-2.5 py-1 rounded-full",
            isClient
              ? "bg-emerald-50 text-emerald-700"
              : "bg-blue-50 text-blue-700"
          )}
        >
          {isClient ? "Client" : "Prospect"}
        </span>
      </div>

      {/* Contact info */}
      <div className="space-y-2 flex-1">
        {company.email && (
          <a
            href={`mailto:${company.email}`}
            className="flex items-center gap-2 text-xs text-slate-500 hover:text-indigo-600 transition-colors group"
          >
            <Mail className="w-3.5 h-3.5 shrink-0 text-slate-400 group-hover:text-indigo-400" />
            <span className="truncate">{company.email}</span>
          </a>
        )}
        {company.phone_number && (
          <div className="flex items-center gap-2 text-xs text-slate-500">
            <Phone className="w-3.5 h-3.5 shrink-0 text-slate-400" />
            <span>{company.phone_number}</span>
          </div>
        )}
        {company.website && (
          <a
            href={company.website}
            target="_blank"
            rel="noreferrer"
            className="flex items-center gap-2 text-xs text-slate-500 hover:text-indigo-600 transition-colors group"
          >
            <Globe className="w-3.5 h-3.5 shrink-0 text-slate-400 group-hover:text-indigo-400" />
            <span className="truncate">{company.website.replace(/^https?:\/\//, "")}</span>
          </a>
        )}
        {!company.email && !company.phone_number && !company.website && (
          <p className="text-xs text-slate-300 italic">Aucune information de contact</p>
        )}
      </div>

      {/* Bottom: projects or opportunities */}
      <div className="mt-5 pt-4 border-t border-slate-100">
        {isClient ? (
          <>
            <p className="text-[10px] font-bold text-slate-400 uppercase tracking-widest mb-2">
              Projets
            </p>
            <div className="flex flex-wrap gap-1.5">
              {company.projects.map((p) => (
                <span
                  key={p.id}
                  className="inline-flex items-center px-2.5 py-0.5 rounded-full text-[11px] font-semibold bg-emerald-50 text-emerald-700 border border-emerald-100"
                >
                  {p.name}
                </span>
              ))}
            </div>
          </>
        ) : (
          <>
            <p className="text-[10px] font-bold text-slate-400 uppercase tracking-widest mb-2">
              Opportunités
            </p>
            {company.opportunities.length > 0 ? (
              <div className="flex flex-wrap gap-1.5">
                {company.opportunities.slice(0, 2).map((o) => (
                  <span
                    key={o.id}
                    className="inline-flex items-center px-2.5 py-0.5 rounded-full text-[11px] font-semibold bg-blue-50 text-blue-700 border border-blue-100"
                  >
                    {o.name}
                  </span>
                ))}
                {company.opportunities.length > 2 && (
                  <span className="text-xs text-slate-400 font-semibold self-center">
                    +{company.opportunities.length - 2}
                  </span>
                )}
              </div>
            ) : (
              <p className="text-xs text-slate-300 italic">Aucune opportunité</p>
            )}
          </>
        )}
      </div>
    </div>
  );
}
